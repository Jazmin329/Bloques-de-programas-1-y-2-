print("-----Operador IGUAL (==)")
print(5 == 5)   # True
print(10 == 7)  # False
print(5.0 == 5) # True

print("\n-----Operador DIFERENTE o NO IGUAL (!=)")
print(5 != 5)   # False
print(10 != 7)  # True
print(5.0 != 5) # False

print("\n-----Operador MAYOR QUE (>)")
print(10 > 5) # True
print(5 > 10) # False

print("\nOperador MENOR QUE (<)")
print(5 < 10) # True
print(10 < 5) # False

print("\nOperador MENOR O IGUAL QUE (<=)")
print(5 <= 5)   # True
print(10 <= 7)  # False
print(3 <= 5)   # True

print("\nOperador MAYOR O IGUAL QUE (>=)")
print(5 >= 5)   # True
print(10 >= 7)  # False
print(3 >= 5)   # True

